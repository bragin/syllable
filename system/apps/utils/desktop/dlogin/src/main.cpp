#include "main.h"

LoginApp::LoginApp() : Application("application./x-vnd.RGC-desktop-login")
{
    pcWindow = new LoginWindow(Rect(0,0,500,300));
    pcWindow->CenterInScreen();
	pcWindow->Show();
    pcWindow->MakeFocus();
}


int main()
{
    LoginApp* thisApp;
    thisApp = new LoginApp();

    thisApp->Run();

}




































































































































